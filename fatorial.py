def calcular_fatorial(n):
  if n == 0:
    return 1
  else:
    return n * calcular_fatorial(n-1)
numero = int(input('Digite  o numero natural: '))
fatorial = calcular_fatorial(numero)
print("O fatorial de' , numero Ã©:",fatorial)